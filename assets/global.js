      var resHtml = "";
      this.cartItems.forEach((item) => {
        resHtml +=
          "<div style='display:flex;'>" +
          new DOMParser()
            .parseFromString(html, "text/html")
            .querySelector(`[id="cart-notification-product-${item.key}"]`)
            .innerHTML +
          "</div>";
      });
